__device__ void findEntry(uint symbol, uint colorScheme, int index, float4 *molecule_arr, uint *color_arr)
{
    for (uint i = 0; i < ELEMENTS; i++)
    {
        // compare symbols
        if (symbol == pse.symbol[i])
        {
            molecule_arr[index].w = pse.rVanDerWaals[i];
            switch (colorScheme)
            {
            case 1:
                color_arr[index] = pse.cCorey[i];
                break;
            case 2:
                color_arr[index] = pse.cKoltun[i];
                break;
            case 3:
                color_arr[index] = pse.cJmol[i];
                break;
            case 4:
                color_arr[index] = pse.cRasmolOld[i];
                break;
            case 5:
                color_arr[index] = pse.cRasmolNew[i];
                break;
            case 6:
                color_arr[index] = pse.cPubChem[i];
                break;
            default:
                color_arr[index] = 0x000000;
                break;
            }
            break;
        }
    }
